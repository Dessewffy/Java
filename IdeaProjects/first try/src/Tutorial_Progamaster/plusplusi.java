package Tutorial_Progamaster;

public class plusplusi {

    public static void main(String[] args) {

        int i = 5;
        System.out.println(i);
        ++i;
        System.out.println(i);
//++i elsőnek hozzáad és csak utána nézi meg az i értékét, a i++ elöbb megnézi és utána ad hozzá.
        i =5;
        System.out.println(i++);
        System.out.println(++i);
    }
}
